            </div>
            <li>Se abrirá un pequeño formulario en donde deberás ingresar tu número de documento y tu correo para posterior a esto dar click en “Verificar”</li>
            <div class="manual-content-paso-a-paso__container-img">
                <img loading="lazy" src="<?php echo(URL_RAIZ);?>/manualUsuario/Imagenes/ModuloInicioDeSesion/restablecerContraseña/2.PNG" alt="">
            </div>
            <li>Luego se enviará a tu correo un código de verificación de cuatro (4) dígitos el cual deberás ingresar en los cuatro campos que veras en pantalla para luego dar click en “Verificar”.</li>
            <div class="manual-content-paso-a-paso__container-img">
                <img loading="lazy" src="<?php echo(URL_RAIZ);?>/manualUsuario/Imagenes/ModuloInicioDeSesion/restablecerContraseña/3.PNG" alt="">
            </div>
