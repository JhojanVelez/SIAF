    <summary class="manual-main-content-detail-content__summary">¿Como registrar un nuevo producto?</summary>
    <div class="manual-content-paso-a-paso">
         <h4 class="manual-content-paso-a-paso__title">Tutorial</h4>
         <p>
            Para poder registrar un producto, por favor ingresa al modulo de productos o a la siguiente ruta 
            <a target="_black" href="<?php echo(URL_RAIZ);?>productos"><?php echo(URL_RAIZ);?>productos</a>
            , luego de ingresar al modulo, seguir los siguientes pasos:
        </p>
        <ol>
            <li>Situarse en la zona de botones que se encuentran debajo de la tabla y el filtro de productos.</li>
            <li>Dar click en el boton de <b>Agregar</b> que se encuentra en la parte infererior de toda la interfaz, siguiente a esto se abrira la ventana de <b>Registra Nuevos Productos</b>.</li>
            <li>
                Llena cada uno de los campos que se encuentran en el formulario (Recuerda que debes llenar todos los campos, de lo contrario no podras avanzar con el proceso y los campos se pintaran de color <b>rojo</b>).
                <br>
                <h5>Nota</h5>
                <ul>
                    <li><p>Si registras informacion en el campo correspondiente y aun no se quita el color rojo del campo, no te preocupes, simplemente este color se eliminara cuando des click en <b>Añadir</b>.</p></li>
                    <li>Para poder ingresar el nit del proveedor, simplemente este se autocompletara automaticamente cuando selecciones un proveedor en la lista despleglable que se encuentra en el campo <b>Nombre del Proveedor</b>.</li> 
                </ul>
            </li>
            <li>Una vez tengas todos los campos diligenciados, debes dar click en <b>Añadir</b> para poder continuar con el proceso de registro.</li>
            <li>Luego se abrira un ventana en donde puedes confirmar los datos que se van a registrar, ademas de esto, si te equivocaste en algun dato, podras dar click en <b>Volver Atras</b> y modificar la informacion que requieras.</li>
            <li>Verificada la informacion, dar click en <b>Confirmar</b> para que se realice el proceso de registro de la informacion.</li> 
            <li>Luego se mostrara un ventana emergete del resultado y culminaras el proceso de registro del producto simplemente dando click en <b>Ok</b>.</li>
        </ol>
    </div>
</details>
