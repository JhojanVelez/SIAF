                <li>Se mostrarán las entradas que de registraron desde el 03/11/2021 a las 03:22 PM hasta el 03/04/2022 a las 03:22 PM.</li>
            </ol>
            <p>Y así es como puedes ir jugando con distintos valores del filtro de búsqueda y obtener los resultados que desees.</p>
