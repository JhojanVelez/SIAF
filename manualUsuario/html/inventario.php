        <div class="manual-content-paso-a-paso">
            <h4 class="manual-content-paso-a-paso__title">Tutorial</h4>
            <p>Para poder registrar nuevas entradas en el sistema de información SIAF, es necesario que tengas en cuenta los siguientes pasos:</p>
            <ol>
                <li>Dar click en el botón con el signo “+” el cual está situado abajo a la derecha de la interfaz.</li>
                <div class="manual-content-paso-a-paso__container-img">
                    <img loading="lazy" src="<?php echo(URL_RAIZ);?>/manualUsuario/Imagenes/ModuloInventario/ModuloEntradas/Registrar/1.png" alt="">
                </div>
                <li>Posterior a esto se abrirá la ventana de Registra Nuevas Entradas.</li>
                <li>Llena cada uno de los campos que se encuentran en el formulario. Una vez tengas todos los campos diligenciados, debes dar click en “Añadir” para poder continuar con el proceso de registro.</li>
                <div class="manual-content-paso-a-paso__container-img">
                    <img loading="lazy" src="<?php echo(URL_RAIZ);?>/manualUsuario/Imagenes/ModuloInventario/ModuloEntradas/Registrar/2.png" alt="">
                </div>
                <li>Luego se abrirá una ventana en donde puedes confirmar los datos que se van a registrar. Verificada la información, dar click en Confirmar para que se realice el proceso de registro de la información.</li>
                <li>Luego se mostrará una ventana emérgete del resultado y culminaras el proceso simplemente dando click en Ok.</li>
                <div class="manual-content-paso-a-paso__container-img">
                    <img loading="lazy" src="<?php echo(URL_RAIZ);?>/manualUsuario/Imagenes/ModuloInventario/ModuloEntradas/Registrar/3.png" alt="">
                </div>
            </ol>
        </div>
