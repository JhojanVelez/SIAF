                </div>
                <li>En el caso de que quieras obtener un conjunto de entradas por periodo de tiempo, simplemente deberás asignar un rango de fecha, en donde, en el campo “Desde” deberás elegir la fecha inicial y en el campo “Hasta” representa la fecha final.</li>
                <div class="manual-content-paso-a-paso__container-img">
                    <img loading="lazy" src="<?php echo(URL_RAIZ);?>/manualUsuario/Imagenes/ModuloInventario/ModuloEntradas/Buscar/3.png" alt="">
                </div>
                <li>Para poder establecer un valor, deberás oprimir el icono del calendario que esta al final del campo.</li>
                <div class="manual-content-paso-a-paso__container-img">
                    <img loading="lazy" src="<?php echo(URL_RAIZ);?>/manualUsuario/Imagenes/ModuloInventario/ModuloEntradas/Buscar/4.png" alt="">
                </div>
                <li>Ahora procederemos a establecer un valor tanto en el campo “Desde” como en el campo “Hasta”</li>
                <div class="manual-content-paso-a-paso__container-img">
                    <img loading="lazy" src="<?php echo(URL_RAIZ);?>/manualUsuario/Imagenes/ModuloInventario/ModuloEntradas/Buscar/5.png" alt="">
